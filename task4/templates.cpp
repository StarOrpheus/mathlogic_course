template<size_t N>
void echo_subst(std::vector<std::string>& out,
                char const* const(&txt)[N],
                ast_expression const& A,
                ast_expression const& B)
{
    std::stringstream ss;
    for (size_t i = 0; i < N; ++i)
    {
        ss.str("");
        ss.clear();
        for (auto it = txt[i]; *it != 0; ++it)
        {
            switch (*it)
            {
            case 'A':
                ss << A;
                break;
            case 'B':
                ss << B;
                break;
            default:
                ss << *it;
                break;
            }
        }

        out.push_back(ss.str());
    }
}

